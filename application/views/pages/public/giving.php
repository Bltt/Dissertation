	<h4>100 Club</h4>
	<p>
		561 ATC run a 100 club that is open to all members, family and friends to join. It costs just £10 per number per year, and each number is entered in the draw every month, with first, second and third receiving cash prizes. Draw runs from April to March (inclusive) annually.
		The more numbers you buy the bigger the chance of winning!
		Half of what is collected goes to Squadron funds and the other half is given away monthly in the PRIZE DRAW.
